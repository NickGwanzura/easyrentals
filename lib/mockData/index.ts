export function getDemoUserByEmail(email: string): User | undefined {
  return demoUsers.find(user => user.email.toLowerCase() === email.toLowerCase());
}

export function getDemoUserById(id: string): User | undefined {
  return demoUsers.find(user => user.id === id);
}

export function validateDemoCredentials(email: string, password: string): User | null {
  // In demo mode, any password of 'demo123' works for demo accounts
  if (password !== 'demo123') return null;
  
  const user = getDemoUserByEmail(email);
  if (!user) return null;
  
  // Check if it's a demo account
  const isDemoAccount = Object.values(DEMO_CREDENTIALS).some(
    cred => cred.email.toLowerCase() === email.toLowerCase()
  );
  
  return isDemoAccount ? user : null;
}

